    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String name, code, sex = "";
        double salary, bonus, income;
        name = txtName.getText().trim();
        code = txtCode.getText().trim();
        if (radbtnMale.isSelected()){
            sex = "Male";
        } else if (radbtnFemale.isSelected())
            sex = "Female";
        
        salary = Double.parseDouble(txtSalary.getText().trim());
        bonus = Double.parseDouble(txtBonus.getText().trim());
        income = salary + bonus;
        Employee em = new Employee(code,name,sex,salary,bonus,income);
        employee.add(em);
        Object []a = {name,code,sex,salary,bonus,income};
        
        g.addRow(a);
        // TODO add your handling code here:
    }//GEN-LAST:event_btnAddActionPerformed
    
    public void clearAll(){
        int n, i;
        n = g.getRowCount();
        for ( i = 0; i < n; i++) {
            g.removeRow(0);
        }
        
    }
