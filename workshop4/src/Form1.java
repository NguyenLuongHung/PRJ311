    public void add (EmNode q){
        while (q != null){
            double income = q.employee.getSalary() + q.employee.getBonus();
            Object []a = {q.employee.getCode(),q.employee.getName(),q.employee.getSex(),q.employee.getSalary(),q.employee.getBonus(),income};
            g.addRow(a);
            q = q.next;
        }
    }
